namespace FloatOrDouble
{
    public class FloatOrDouble
    {
        public static void Main(string[] args)
        {
            double firstDouble = 34.567839023;
            System.Console.WriteLine(firstDouble);

            float firstFloat = 12.345f;
            System.Console.WriteLine(firstFloat);

            double secondDouble = 8923.1234857;
            System.Console.WriteLine(secondDouble);

            float secondFloat = 3456.091f;
            System.Console.WriteLine(secondFloat);
        }
    }
}
